/* USER CODE BEGIN 1 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  static uint8_t cnt16 = 0;
  static bool c2c_cs_fall_flag = false;

  switch (GPIO_Pin)
  {
  case C2C_CS_Pin:
    c2c_cs_fall_flag = true;
    break;
  
  case C2C_SCLK_Pin:
    if(c2c_cs_fall_flag)
    {
      c2c_data <<= 1;
      if(C2C_MOSI_STATE == GPIO_PIN_SET)
      {
        c2c_data |= 1;
      }

      cnt16 += 1;

      if(cnt16 == 16)
      {
        cnt16 = 0;
        c2c_cs_fall_flag = false;
      }
    }
    break;

  default:
    break;
  }
}
