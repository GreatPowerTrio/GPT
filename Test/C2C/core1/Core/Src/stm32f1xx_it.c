uint8_t C2C_Receive_Byte(void)
{
  uint8_t i;
  uint8_t dat = 0;

  for(i = 0; i < 8; i++)
  {
    if(C2C_CLK_STATE == GPIO_PIN_RESET)
    {
      if(C2C_MOSI_STATE == GPIO_PIN_SET)
      {
        dat |= 1;
      }
    }
    dat <<= 1;
  }
  return dat;
}
