uint8_t dat;

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  static uint8_t cnt = 0;
  static bool cs_falling_flag = false;


  switch (GPIO_Pin)
  {
  case GPIO_PIN_1:
    if(HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_1) == GPIO_PIN_RESET)
      HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_5);
    else
      HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_4);
    break;


  case GPIO_PIN_6:
    if(cs_falling_flag == true)
    {
      dat <<= 1;
      if(C2C_MOSI_STATE == GPIO_PIN_SET)
      {
        dat |= 1;
      }

      cnt += 1;

      if(cnt == 8)
      {
        print("%d\r\n", (int)dat);
        // dat = 0;
        cnt = 0;
        cs_falling_flag = false;
      }
    }

    break;
  


  case GPIO_PIN_9:
    cs_falling_flag = true;
    break;


  default:
    break;
  }
}

